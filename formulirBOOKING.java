      public void updateTable() {
          DefaultTableModel tabelsewa = (DefaultTableModel) jTable1.getModel();
          tabelsewa.setRowCount(0);

        // Menambahkan data ke dalam tabel
        for (DataSewaBoss data : dataList) {
            tabelsewa.addRow(new Object[]{
                data.getNama(), 
                data.getJenisLapangan(), 
                data.getTanggalSewa(), 
                data.getJenisHari(), 
                data.getWaktuSewa(), 
                data.getHarga(), 
                data.getPelunasan()
            });
        }        
}
        private void cek(String nama, String jenisLapangan, String tanggal, String hari, String waktuSewa, int harga, String pelunasan) {
        if (tersediakah(tanggal, hari, waktuSewa)) {
        DataSewaBoss booking = new DataSewaBoss(nama, jenisLapangan, tanggal, hari, waktuSewa, harga, pelunasan);
        dataList.add(booking);

            updateTable();

        JOptionPane.showMessageDialog(this, "Pemesanan berhasil!");
        } else {
            JOptionPane.showMessageDialog(this, "Pemesanan gagal! Waktu tersebut sudah terbook.", "Error", JOptionPane.ERROR_MESSAGE);
        }
}
       private boolean tersediakah(String tanggal, String hari, String waktuSewa) {
        System.out.println("Memeriksa ketersediaan untuk: " + tanggal + " " + hari + " " + waktuSewa);

        for (DataSewaBoss data : dataList) {
            System.out.println("Data dalam list: " + data.getTanggalSewa() + " " + data.getJenisHari() + " " + data.getWaktuSewa());
            if (data.getTanggalSewa().equals(tanggal) && data.getJenisHari().equals(Hari) && data.getWaktuSewa().equals(waktuSewa)) {
               return false; 
        }
    }
        return true;
    }
    

    

