    private void BookingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BookingActionPerformed
          
    if (NamaPenyewa.getText().isEmpty()) {
        DefaultTableModel tabelsewa = (DefaultTableModel) jTable1.getModel();        
        JOptionPane.showMessageDialog(this, "Nama Penyewa tidak boleh kosong!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
        }
         

        String nama = NamaPenyewa.getText();
        String jenisLapangan = (String) JnsLap.getSelectedItem();
        String tanggal = (String) this.tanggal.getSelectedItem();
        String bulan = (String) this.bulan.getSelectedItem();
        String tahun = (String) this.tahun.getSelectedItem();
        String tanggalSewa = tanggal + " " + bulan + " " + tahun;
        String hari = (String) Hari.getSelectedItem();
        String jamMulai = (String) mulai.getSelectedItem();
        String jamSelesai = (String) selesai.getSelectedItem();
        String waktuSewa = jamMulai + " - " + jamSelesai;
        
        String metodePembayaran = "";
        if (cash.isSelected()) {
        metodePembayaran = "Cash";
        } else if (transfer.isSelected()) {
        metodePembayaran = "Transfer";
        } else {
        JOptionPane.showMessageDialog(this, "Silakan pilih metode pembayaran!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
        }
        
        if (tersediakah(tanggalSewa, hari, waktuSewa)) {
        } else {
            JOptionPane.showMessageDialog(this, "Pemesanan gagal! Waktu tersebut sudah terbook.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        
        System.out.println("Nama Penyewa: " + nama);
        System.out.println("Jenis Lapangan: " + jenisLapangan);
        System.out.println("Tanggal Sewa: " + tanggalSewa);
        System.out.println("Jenis Hari: " + hari);
        System.out.println("Waktu Sewa: "+waktuSewa+"");
        System.out.println("Metode Pembayaran: " + metodePembayaran);
        
        int getHarga = Integer.parseInt(hargaLapangan.getText());
        int getHari = Integer.parseInt(hargaPerJam.getText());
        int Harga = getHarga + getHari;
        String putHarga = Integer.toString(Harga);
        
        harga.setText(putHarga);
   
        int jamMulaiInt = Integer.parseInt(jamMulai);  
        int jamSelesaiInt = Integer.parseInt(jamSelesai);     
        int durasiSewa = jamSelesaiInt - jamMulaiInt;
        

        
        // Perhitungan total harga
        int totalHarga = Harga * durasiSewa;
        Tott.setText("" + totalHarga);
        
        // Menambahkan Data Ke Tabel
        tabelsewa.addRow(new Object[]{nama, jenisLapangan, tanggalSewa, hari, waktuSewa, totalHarga, metodePembayaran});        
        DataSewaBoss data = new DataSewaBoss(nama, jenisLapangan, tanggalSewa, hari, waktuSewa, Harga, metodePembayaran);
        
        // Menambahkan data ke tabel history
        if (Daftar1 != null) {
        Daftar1.updateTable(nama, jenisLapangan, tanggalSewa, hari, waktuSewa, Harga, metodePembayaran);
        } 

        dataList.add(data);
   

    }//GEN-LAST:event_BookingActionPerformed

