    private void HapusDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HapusDataActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Silakan pilih data yang ingin dihapus.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        DefaultTableModel tabelsewa = (DefaultTableModel) jTable1.getModel();
        tabelsewa.removeRow(selectedRow);
        
        if (selectedRow < dataList.size()) {
        dataList.remove(selectedRow);
        }
    }//GEN-LAST:event_HapusDataActionPerformed

    private void editDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editDataActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Silakan pilih data yang ingin diedit.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String nama = (String) tabelsewa.getValueAt(selectedRow, 0);
        String jenisLapangan = (String) tabelsewa.getValueAt(selectedRow, 1);
        String tanggalSewa = (String) tabelsewa.getValueAt(selectedRow, 2);
        String hari = (String) tabelsewa.getValueAt(selectedRow, 3);
        String waktuSewa = (String) tabelsewa.getValueAt(selectedRow, 4);
        int totalHarga = (int) tabelsewa.getValueAt(selectedRow, 5);
        String metodePembayaran = (String) tabelsewa.getValueAt(selectedRow, 6); 
        
        NamaPenyewa.setText(nama);
        JnsLap.setSelectedItem(jenisLapangan);
        String[] tanggalArray = tanggalSewa.split(" ");
        tanggal.setSelectedItem(tanggalArray[0]);
        bulan.setSelectedItem(tanggalArray[1]);
        tahun.setSelectedItem(tanggalArray[2]);
        Hari.setSelectedItem(hari);
        String[] waktuArray = waktuSewa.split(" - ");
        mulai.setSelectedItem(waktuArray[0]);
        selesai.setSelectedItem(waktuArray[1]);

       
        tabelsewa.removeRow(selectedRow);
    }//GEN-LAST:event_editDataActionPerformed

